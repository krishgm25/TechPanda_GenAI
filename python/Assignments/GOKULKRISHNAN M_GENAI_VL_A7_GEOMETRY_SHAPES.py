triangle = [(2,5),(3,4),(3,6),(4,3),(4,7),(5,2),(5,3),(5,4),(5,5),(5,6),(5,7),(5,8)]

def triangle_shape():
    print('Triangle')
    for i in range(1,10):
        for j in range(1,10):
            if (i,j) in triangle:
                print('+',end=' ')
            else:
                print(' ',end=' ')
        print()

